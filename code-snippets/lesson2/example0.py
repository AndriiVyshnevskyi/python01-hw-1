# Integer
x = 5
print(x)

# Float
y = 3.14
print(y)

# Complex
z = 2 + 3j
print(z)

# List
my_list = [1, 2, 3]
print(my_list)

# Tuple
my_tuple = (4, 5, 6)
print(my_tuple)

# Range
my_range = range(1, 10, 2)
print(list(my_range))

# String
my_string = "Hello, World!"
print(my_string)

# Dictionary
my_dict = {"name": "John", "age": 25}
print(my_dict)


# Set
my_set = {1, 2, 3}
print(my_set)

# Boolean
is_true = True
is_false = False
print(is_true, is_false)
